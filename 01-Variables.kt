fun main(){

    //Java, variables mutables 
    Int a = 2;
    Int b = 3;
    Int c =  a + b;

    //kotlin
    var a:Int = 2
    var b:Int = 3
    var c:Int = a + b

    //Inferencia de tipo de variable, kotlin infiere en que el tipo de dato es flotante
    
    var x = 3.2

    //Se usa var para indicar que la variable es mutable

    //Se usa val para variables de tipo inmutable
    
    val x = 2

}